package saci;

public class Tuple5<F1, F2, F3, F4, F5> { 
	
	  public final F1 f1; 
	  public final F2 f2; 
	  public final F3 f3;
	  public final F4 f4;
	  public final F5 f5;
	  public Tuple5(F1 x, F2 f2, F3 f3, F4 f4, F5 f5) { 
	    this.f1 = x; 
	    this.f2 = f2;
	    this.f3 = f3;
	    this.f4 = f4;
	    this.f5 = f5;
	  } 
}

